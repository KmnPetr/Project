CREATE TABLE Person(
    id int GENERATED BY DEFAULT AS IDENTITY,
    username varchar(30) UNIQUE NOT NULL,
    password varchar NOT NULL,
    role varchar(30) NOT NULL,
    created_at timestamp NOT NULL
);

